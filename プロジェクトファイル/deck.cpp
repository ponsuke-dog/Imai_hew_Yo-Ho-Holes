Deck::Deck(Ship* ship)
{
	SetMap(ship);
}


Deck::~Deck()
{
}

void Deck::SetMap(Ship* ship)
{
	XMFLOAT3 StartPosition = ship->GetCenterPosition();
	StartPosition.x += -MAP_BLOCK_SIZE * 7;
	StartPosition.z += MAP_BLOCK_SIZE * 13;

	int count = 0;

	for (int z = 0; z < MAP_HEIGHT_NUM; z++)
	{
		for (int x = 0; x < MAP_WIDTH_NUM; x++)
		{
			ship->RegisterDeckBlocks(new DeckBlock(ship,XMFLOAT3(StartPosition.x + (MAP_BLOCK_SIZE * x), StartPosition.y, StartPosition.z - (MAP_BLOCK_SIZE * z)),
				(DeckType)g_MapData[ship->GetTeamId()][z][x], count));


			count++;
		}
	}
}
